-- 4. FunciÃ³n y MÃ¡quina de Estados (Triple Candado de Seguridad)
CREATE OR REPLACE FUNCTION auditar_estado_boleta()
RETURNS TRIGGER AS $$
BEGIN
    -- LÃ³gica de transiciÃ³n de estado hacia REGISTRADA (3)
    IF NEW.estado = 3 THEN
        
        -- Candado 1: El estado inmediatamente anterior DEBE ser ACTIVA (2)
        IF OLD.estado != 2 THEN
            RAISE EXCEPTION 'ViolaciÃ³n de SEGURIDAD: La boleta % no puede ser REGISTRADA sin estar previamente ACTIVA en punto de venta. Estado anterior: %', NEW.id_boleta, OLD.estado;
        END IF;

        -- Candado 2: Cumplimiento Ley 1581 (Habeas Data) obligatorio
        IF NEW.habeas_data_aceptado IS NOT TRUE THEN
            RAISE EXCEPTION 'ViolaciÃ³n de POLÃTICA DE DATOS: La boleta % requiere la aceptaciÃ³n expresa de las polÃ­ticas de privacidad y tratamiento de datos (Ley 1581).', NEW.id_boleta;
        END IF;

        -- Candado 3: SelecciÃ³n de premio obligatoria (consulta al menÃº desplegable)
        IF NEW.premio_seleccionado IS NULL THEN
            RAISE EXCEPTION 'ViolaciÃ³n de REGLA NEGOCIO: La boleta % no ha seleccionado un premio dinÃ¡mico.', NEW.id_boleta;
        END IF;

        -- Setear el timestamp exacto del registro
        NEW.fecha_registro = NOW();
    END IF;

    -- Registrar Timestamp cuando se activa la boleta (Estado 2)
    IF NEW.estado = 2 AND OLD.estado != 2 THEN
        NEW.fecha_activacion = NOW();
    END IF;

    -- ActualizaciÃ³n de Ãºltimo cambio
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

