RETURNS INTEGER AS $$
DECLARE
    tiene_permiso BOOLEAN;
    filas_afectadas INTEGER;
BEGIN
    -- AuditorÃ­a DB-First: Permite rol 'admin' u 'operativo' asignar lotes
    SELECT EXISTS (
        SELECT 1 FROM public.perfiles 
        WHERE id = p_admin_id AND rol IN ('admin', 'operativo')
    ) INTO tiene_permiso;
    
    IF NOT tiene_permiso THEN
        RAISE EXCEPTION 'SEGURIDAD: OperaciÃ³n denegada. El ID proporcionado no posee permisos de asignaciÃ³n (admin u operativo).';
    END IF;

